Evening: Take a walking tour around the old sections of Hoi An Ancient Town (charming and wonderfully preserved, combining Chinese, Japanese and European influences) and get yourself back to the 17th century. Shopping around the ancient city. After dinner, return to Da Nang. Overnight in Da Nang.

  
